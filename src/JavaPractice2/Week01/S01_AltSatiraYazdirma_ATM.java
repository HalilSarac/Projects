package JavaPractice2.Week01;

public class S01_AltSatiraYazdirma_ATM {
    public static void main(String[] args) {
        /*
        *********************************************
                    "ATM'ye Hosgeldiniz"
        1- Bakiye Sorgulama
        2- Para Yatirma
        3- Para Cekme
        4- Menuden Cikis
        **********************************************
         */
        /* \n  : Metni bir alt satırdan itibaren yazdırır.
              \t  : Metni yatayda 1 tab miktarı kaydırır.
              \\  : \  Ters slash yazdırır
              \'  : '  Tek tırnak yazdırır.
              \"  : "  Çift tırnak yazdırır.
         */
        System.out.println("*********************************************\n\t\t\t"+
                "\"ATM'ye Hosgeldiniz\"\n"+
                "1- Bakiye Sorgulama\n"+
                "2- Para Yatirma\n"+
                "3- Para Cekme\n"+
                "4- Menuden Cikis\n"+
                "**********************************************");
    }
}

